     <!-- <h3><el-icon><finished /></el-icon> 项目概述</h3> -->
  <p>《〈资本论〉汉英术语知识库》是2018年国家社会科学基金项目《〈资本论〉汉英术语知识库建设与应用研究》（项目号：18BTQ025）的研究成果。</p>
 
   <p>
    作为马克思主义政治经济学集大成之作，《资本论》被公认为全世界“工人阶级的圣经”（恩格斯语）。习近平总书记在纪念马克思诞辰200周年大会上特别强调，“1876年问世的《资本论》是马克思主义最厚重、最丰富的著作”。《资本论》全面阐释了马克思主义政治经济学的范畴、概念和基本原理，构成中国特色社会主义政治经济学的理论基础。对《资本论》承载的核心术语的准确理解、把握是中国特色社会主义政治经济学话语体系建设的根本前提。
   </p>
 
 <p>
    术语是人类科学知识在语言中的结晶，不仅表述专业领域中特定概念的语言特征，还是知识组织的基础和形式。术语知识库（terminological knowledge base，简称TKB）最早由加拿大渥太华大学Ingrid Meyer等学者于1992年提出，是术语学与知识工程学相结合的产物，目的是克服传统术语库仅能描述语言信息的窠臼，努力表征特定专业领域术语的语言和语言外知识，通过把概念组织为复杂的关系网络，厘清术语间的概念关系，从而将知识描述、知识融合、知识发现、知识可视化呈现融为一体，成为信息化时代不可或缺的面向全社会专业知识需求者的语言服务基础设施。
   </p>
 <p>
    《〈资本论〉汉英术语知识库》借鉴世界顶尖的加拿大Termium Plus术语管理系统，选取中央编译局1974年《马克思恩格斯全集》第一版的三卷本《资本论》和2003年《马克思恩格斯全集》第二版的三卷本《资本论》，以及英国劳伦斯·维沙特出版社（Lawrence & Wishart Publishers）出版的国际权威英语版Marx Engels Collected Works的三卷本《资本论》，建立汉英双语平行语料库，以“计算机提取+专家人工筛选”相结合的方式，同时参考国内已有的专业文献（含各类《资本论》辞典），确定包括1738个词条的《〈资本论〉术语清单》，内容涵盖：①专有名词；②重要人物；③经典著作；④舆论阵地；⑤重要历史事件；⑥《资本论》典故。
   </p>

 <p>
   具体到每一术语条目，则包括下列内容：①术语的汉语名称；②术语权威英译方案；③术语释义与参考文献。释义使用中文陈述，但陈述过程中给出所涉外文关键词/句，以便于用户理解和知识挖掘；④术语语境信息。分为精准语境信息和大数据语境信息。前者提供《资本论》中包含该术语的英语例句及中央编译局两个权威中文版的对应译文，后者则基于大数据挖掘技术，在CNKI学术论文数据源中搜索包含术语的双语例句，向用户提供经典作品以外的平行文本作为参考，构成“其他人还在用”功能板块。该板块的例句虽未必精准，但能为用户提供更多使用参考；⑤术语主题图。利用术语管理技术和知识本体技术，借助主题图描述特定术语在《资本论》知识体系中的位置和与其他术语间的概念关系；⑥术语核心词拓展。以特定术语为核心，给出可以衍生出的其他表述方式或特定用法（中、英双语）。
   </p>

 <p>
   《〈资本论〉汉英术语知识库》可实现术语内容的快速查询，并较为准确地呈现相关术语在《资本论》知识体系中的具体位置及与其他术语的知识关联。毫无疑问，这一智慧型开放资源不仅有效提升我国公共图书馆知识服务的水平，还特别有助于中国特色社会主义政治经济学话语体系的理论创新和国际传播。
   </p>

 <p>
   作为跨学科研究成果，《〈资本论〉汉英术语知识库》的研制得益于马克思主义政治经济学、翻译学、计算机科学、图书馆学等多领域研究团队的协同攻关，其用户包括致力于马克思主义中国化著作英译的专业译者，从事经典马克思主义、西方马克思主义及中国特色社会主义政治经济学研究的科研人员，涉足当代中国文化、学术成果外译的语言服务企业，以及广大的英语和翻译学习者，可在一定程度上弥补我国当前缺乏专业领域术语管理系统的缺憾，同时为后续术语知识库的规模化开发积累经验。
      </p>
